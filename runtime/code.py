	if not _zero_map_.has_key(s):
		if s[-1] == 0:
			dtype = tf.int32
		else:
			dtype = tf.float32
		shape = s[0:-1]
		if len(shape) == 3:
			shape = (shape[0],shape[2])
		_zero_map_[s] = tf.zeros(shape, dtype)
	return _zero_map_[s]
